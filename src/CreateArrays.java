import java.util.Arrays;

public class CreateArrays {
    public static void main(String[] args){
        // 5 Numbers
        int[] numbers = {1, 21, 63, 4, 51};
        System.out.println("Numbers:" + Arrays.toString(numbers));

        // 7 Strings
        String[] sevenStrings = {"Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"};
        System.out.println("Strings: " +Arrays.toString(sevenStrings));

        //5 Characters
        char[] userChar= {'a', 'e', 'i', 'o', 'u'};
        System.out.println("Characters: " + Arrays.toString(userChar));

        //2 Booleans
        boolean[] bool={true, false};
        System.out.println("Booleans: " +Arrays.toString(bool));







    }
}
